return {
	{ "nvim-treesitter/playground", cmd = "TSPlaygroundToggle" },

	{
		"nvim-treesitter/nvim-treesitter",
		opts = {
			ensure_installed = {
				"astro",
				"cmake",
				"cpp",
				"c",
				"css",
				"fish",
				"gitignore",
				"go",
				"graphql",
				"http",
				"java",
				"php",
				"rust",
				"scss",
				"sql",
				"svelte",
			},

			-- Force highlighting to be enabled
			highlight = {
				enable = true,
				additional_vim_regex_highlighting = false,
			},

			-- matchup = {
			-- 	enable = true,
			-- },

			-- https://github.com/nvim-treesitter/playground#query-linter
			query_linter = {
				enable = true,
				use_virtual_text = true,
				lint_events = { "BufWrite", "CursorHold" },
			},

			playground = {
				enable = true,
				disable = {},
				updatetime = 25, -- Debounced time for highlighting nodes in the playground from source code
				persist_queries = true, -- Whether the query persists across vim sessions
				keybindings = {
					toggle_query_editor = "o",
					toggle_hl_groups = "i",
					toggle_injected_languages = "t",
					toggle_anonymous_nodes = "a",
					toggle_language_display = "I",
					focus_language = "f",
					unfocus_language = "F",
					update = "R",
					goto_node = "<cr>",
					show_help = "?",
				},
			},
		},
		config = function(_, opts)
			require("nvim-treesitter.configs").setup(opts)

			-- MDX
			vim.filetype.add({
				extension = {
					mdx = "mdx",
				},
			})
			vim.treesitter.language.register("markdown", "mdx")

			-- Force treesitter to start on buffer events
			vim.api.nvim_create_autocmd({ "BufRead", "BufNewFile", "BufEnter" }, {
				callback = function()
					local buf = vim.api.nvim_get_current_buf()
					-- Force filetype detection
					vim.bo[buf].filetype = vim.bo[buf].filetype
					-- Force treesitter to start if available
					if vim.treesitter.highlighter.active[buf] == nil then
						vim.defer_fn(function()
							pcall(vim.treesitter.start, buf)
						end, 50)
					end
				end,
			})
		end,
	},
}
